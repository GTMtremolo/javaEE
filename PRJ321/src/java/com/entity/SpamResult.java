package com.entity;

import java.util.Date;

/**
 *
 * @author snail
 */
public class SpamResult {
    String email;
    Date date;
    boolean sent;

    public SpamResult(String email, Date date, boolean sent) {
        this.email = email;
        this.date = date;
        this.sent = sent;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }

    public boolean isSent() {
        return sent;
    }

    public void setSent(boolean sent) {
        this.sent = sent;
    }
        
}
